from colors import detect_main_colors
from clustering import cluster_images
from classifier import train_cat_dog_classifier, predict_single_image

# Това е главният файл на приложението, който предоставя меню за избор на различни функции, свързани с машинното обучение. Потребителят може да избере да открие основните цветове в снимка, да извърши клъстеризация на снимки без учител, да обучи модел за класификация на котки и кучета или да разпознае една снимка като котка или куче. Менюто се повтаря, докато потребителят не избере опцията за изход. Този файл служи като интерфейс за потребителя, който обединява всички функции от другите модули (colors.py, clustering.py и classifier.py) и ги прави достъпни чрез лесен за използване текстов интерфейс. Това е основният вход в приложението, който управлява потока на програмата и взаимодействието с потребителя.
def print_menu():
    print("\n==============================")
    print("Приложение по Машинно обучение")
    print("==============================")
    print("1. Откриване на 5 основни цвята")
    print("2. Клъстеризация на снимки (без учител)")
    print("3. Обучение за котка/куче (с учител)")
    print("4. Разпознаване на 1 снимка котка/куче")
    print("5. Изход")

# Главната функция, която управлява менюто и извиква съответните функции в зависимост от избора на потребителя. Тя съхранява обучен модел в променлива, която може да бъде използвана за разпознаване на снимки след обучението. Менюто се повтаря, докато потребителят не избере опцията за изход, което позволява на потребителя да изпълни няколко различни задачи в рамките на една сесия. Тази функция е основният контролер на приложението, който координира взаимодействието между потребителя и различните функционалности, предоставени от другите модули. Тя гарантира, че потребителят може лесно да навигира през различните опции и да използва функциите за машинно обучение по интуитивен начин.
def main():
    model = None

    while True:
        print_menu()
        choice = input("Избери опция: ").strip()

        if choice == "1":
            print("\nПримерен път: sample_images/test.jpeg")
            image_path = input("Въведи път до снимката: ")
            detect_main_colors(image_path, k=5)

        elif choice == "2":
            print("\nЩе използвам папките data/cats и data/dogs, но без етикети.")
            folder_list = ["data/cats", "data/dogs"]
            cluster_images(folder_list)

        elif choice == "3":
            print("\nЗапочва обучение на модела...")
            model = train_cat_dog_classifier("data/cats", "data/dogs")

            if model is not None:
                print("Моделът е обучен успешно.")

        elif choice == "4":
            if model is None:
                print("Първо трябва да обучиш модела от опция 3.")
            else:
                print("\nПримерен път: data/test/1.jpeg")
                image_path = input("Въведи път до тестовата снимка: ")
                predict_single_image(model, image_path)

        elif choice == "5":
            print("Край на програмата.")
            break

        else:
            print("Невалиден избор. Опитай пак.")

# Този блок гарантира, че главната функция ще се изпълни само когато този файл бъде стартиран директно, а не когато бъде импортиран като модул в друг файл. Това е стандартна практика в Python, която позволява на кода да бъде използван както като скрипт, така и като библиотека. Ако този файл бъде импортиран в друг файл, кодът в блока `if __name__ == "__main__":` няма да се изпълни, което предотвратява нежелано поведение при импортиране. Това е важно за този проект, тъй като функциите за класификация, клъстеризация и откриване на цветове могат да бъдат използвани и в други контексти, без да се стартира главното меню. Това позволява на разработчика да използва функциите от този файл в други части на проекта или в други проекти, без да се налага да изпълнява главното меню всеки път.
if __name__ == "__main__":
    main()